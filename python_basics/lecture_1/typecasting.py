#  Type casting

name = "micheal"
age = 18
grade = 3.7

print(type(name), type(age), type(grade))

# Typecasting to string

age = str(age)
grade = str(grade)

print(type(name), type(age), type(grade))

# Typecasting to int

name = int(name, base=25)
grade = int(grade)

print(type(name), type(age), type(grade))


# Typecasting to Float

name = "20.775"
print(type(name))

name = float(name)
age = float(age)

print(type(name), type(age), type(grade))

