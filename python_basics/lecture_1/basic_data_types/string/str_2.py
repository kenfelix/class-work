# Indexing strings

greet = "Hello"

# to idex we use the "[]"

first_letter = greet[0]
second_letter = greet[1]
third_letter = greet[2]
fourth_letter = greet[3]
last_letter = greet[4]

print(first_letter, second_letter, third_letter, fourth_letter, last_letter)

# getting the length of a string.

str_length = len(greet)

print(str_length)

