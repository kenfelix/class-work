#  integers: used to represent whole numbers

x = 100
y = 200
z = 20

print(type(y))

"""
    addition,  +
    subtraction, -
    multiplication, *
    division, /
    floor division, //
    Power (Exponent), **
    Modulus, %
"""

print(x+y)
print(x-z)
print(100-50)
print(y*z)
print(y/x, type(y/x))
print(x/3)
print(y//x, type(y//x))
print(x//3)
print(1/2, 1//2)

print(x**2)
print(z**5)

print(y%x)  # print 0
print(z%7)  # print 8