# If statement:

"""
    if <condtion>:
        <command>  
"""
a = 33
b = 200
if b > a:
    print("b is greater than a")

#  if else statement

"""
    if <condtion>:
        <command>
    else:
        <command>
"""

a = 33
b = 33
if b > a:
   print("b is greater than a")
else:
   print("not in this scope")


#  nested if else statement

"""
    if <condtion>:
        <command>
    elif <condition>:
        <command>
    .
    .
    .
    else:
        <command>
"""

a = 33
b = 33
if b > a:
   print("b is greater than a")
elif a == b:
   print("a is equal to b")
else:
   print("not in this scope")
      
    




